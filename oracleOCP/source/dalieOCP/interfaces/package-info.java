/**
 * <h6>Interfaces</h6>
 * 
 * <p>An interface is a reference type in Java, <br/>
 * it is similar to class, it is a collection of abstract methods.<br/> 
 * A class implements an interface, <br/> 
 * thereby inheriting the abstract methods of the interface.</p>
 * 
 * <ul>
 * <li>You cannot instantiate an interface.</li>
 * <li>An interface does not contain any constructors.</li>
 * <li>All of the <b>methods</b>  in an interface are <b>abstract</b>.</li>
 * <li>An interface cannot contain instance fields. <br/> 
 * 		The only <b>fields</b> that can appear in an interface must be declared <br/>
 * 		<b>both static and final</b>.</li>
 * <li><b>Each method</b> in an interface is also <b>implicitly abstract</b>,<br/> 
 * 		so the abstract keyword is not needed.</li>
 * <li><b>Methods</b> in an interface are <b>implicitly public</b>.</li>
 * <li>An interface can extend multiple interfaces.</li>
 * </ul>
 * 
 * @author dalie.de
 *
 */
