/**
 * <h6>Object-Relationship</h6>
 * 
 * <p>IS-A and HAS-A</p>
 * 
 * <ul>
 * 	<li><b>IS-A</b>&nbsp;This relationship is implemented when
 * 		<ul>
 * 			<li>Class-extends another class (derived class IS-A base class)</li>
 * 			<li>An interface extends another interface (derived interface IS-A base interface)</li>
 * 			<li>Class-implements an interface (class IS-A implemented interface)</li>
 * 		</ul>
 * 	</li>
 * 	<li><b>HAS-A</b>&nbsp;This relationship is implemented by defining an instance variable.
 * 		<ul>
 * 			<li>Class—defines an instance variable of another class</li>
 * 			<li>Class—defines an instance variable of an interface</li>
 * 		</ul>
 * 	</li>
 * </ul>
 * 
 * @author dalie.de
 *
 */
